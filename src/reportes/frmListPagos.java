    public void llenarData(ArrayList<clsPago> dataPago, String tipo)
    {
        //Double totalEfectivo = 0.00;        
        
        DecimalFormat df1 = new DecimalFormat("##0.00"); 
       
            int maxData = dataPago.size();  
            String fecha_realizada = "";
            String fecha_registro = "";

            for(int i=0; i<maxData; i++)
            {                    
                 //fecha_realizada = dataPago.get(i).getFechaPago().substring(0, 16);    
                 fecha_realizada = dataPago.get(i).getFechaPago().substring(0, 16);    
                 if( dataPago.get(i).getFechaRegistro()==null)
                 {
                     fecha_registro="";
                 }
                 else
                     fecha_registro = dataPago.get(i).getFechaRegistro().substring(0, 16);    
                 Object[] nuevaFila = {  i+1,                          
                                        fecha_realizada,
                                        dataPago.get(i).getNombreCliente(),
                                        "$ " + df1.format(dataPago.get(i).getValor()), 
                                        dataPago.get(i).getNombreUsuario(),   
                                        dataPago.get(i).getReferencia(),
                                        fecha_registro,
                                        dataPago.get(i).getIdPago(),
                                        tipo
                 };        

                 totalEfectivo = totalEfectivo + dataPago.get(i).getValor();             
                 dtmData.addRow(nuevaFila); 
            }         
            txtTotal.setText("" + (objUtils.redondear(totalEfectivo)));     
        
    }
     
    public void buscar_informacion()
    {
        objUtils.vaciarTabla(dtmData);        

        String fechaInicio = "";
        String fechaFin = "";
        
        DateFormat df1 = new SimpleDateFormat("yyyy-MM-dd");
        
            
        if(this.chkFechaPago.isSelected())
        {   
            Date date1 = txtFechaInicio.getDate();
            fechaInicio = df1.format(date1);

            Date date2 = txtFechaFin.getDate();
            fechaFin = df1.format(date2);
        
            if(chkNE.isSelected())
            {  
                /*ArrayList<clsPago> dataPago = objPago.consultaDataPagosFecha(fechaInicio, fechaFin); 
                if(dataPago.isEmpty())
                {
                    JOptionPane.showMessageDialog(this, "No existen datos", "AtenciÃ³n!", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                    llenarData(dataPago, "NE");
                }*/
                ArrayList<clsPago> dataPago = objPago.consultaDataPagosFecha(fechaInicio, fechaFin); 
                llenarData(dataPago, "NE");
            }       
            if(chkFacturas.isSelected())
            {  
                ArrayList<clsPago> dataPago2 = objPago.consultaDataPagosFacturaFecha(fechaInicio, fechaFin);                    
                llenarData(dataPago2, "FACTURA");    
            }     
            if(chkVarios.isSelected())
            {  
                ArrayList<clsPago> dataPago3 = objPago.consultaDataPagosOtrosRegistroFecha(fechaInicio, fechaFin);                     
                llenarData(dataPago3, "OTROS");    
            }   
            
            
        }   
        else if (this.chkFechaRegistro.isSelected())
        {
            Date date1 = txtFechaInicioRegistro.getDate();
            fechaInicio = df1.format(date1);

            Date date2 = txtFechaFinRegistro.getDate();
            fechaFin = df1.format(date2);
            
            if(chkNE.isSelected())
            {  
                ArrayList<clsPago> dataPago = objPago.consultaDataPagosRegistroFecha(fechaInicio, fechaFin); 
                llenarData(dataPago, "NE");
            }       
            if(chkFacturas.isSelected())
            {  
                ArrayList<clsPago> dataPago2 = objPago.consultaDataPagosFacturaRegistroFecha(fechaInicio, fechaFin);                    
                llenarData(dataPago2, "FACTURA");    
            }     
            if(chkVarios.isSelected())
            {  
                ArrayList<clsPago> dataPago3 = objPago.consultaDataPagosOtrosRegistroFecha(fechaInicio, fechaFin);                     
                llenarData(dataPago3, "OTROS");    
            }   
            
        }
        else
        {
            if(chkNE.isSelected())
            {  
                ArrayList<clsPago> dataPago = objPago.consultaDataPagos();                      
                llenarData(dataPago, "NE");    
            }       
            if(chkFacturas.isSelected())
            {  
                ArrayList<clsPago> dataPago2 = objPago.consultaDataPagosFactura();                      
                llenarData(dataPago2, "FACTURA");    
            }     
            if(chkVarios.isSelected())
            {  
                ArrayList<clsPago> dataPago3 = objPago.consultaDataPagosOtros();                      
                llenarData(dataPago3, "OTROS");    
            }     
        }
    }  
    
